package lx.entity;

public class User {
	//定义私有参数
	private int uid;
	private String uname;
	
	//封装
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public String getUname() {
		return uname;
	}
	public void setUname(String uname) {
		this.uname = uname;
	}
	
	
	
}
